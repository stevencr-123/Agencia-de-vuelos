    private void btnContratarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContratarActionPerformed
        
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        String correo = txtCorreo.getText();
        String telefono = txtTelefono.getText();
        String avion = (String) comboBoxavion.getSelectedItem();


        if (nombre.isEmpty() || apellidos.isEmpty() || correo.isEmpty() || telefono.isEmpty() || avion == null) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            PilotoDAO pilotoDAO = new PilotoDAO();

            LocalDate fechaContratacion = LocalDate.now();

            pilotoDAO.contratarPiloto(nombre, apellidos, correo, telefono, fechaContratacion, avion);

            JOptionPane.showMessageDialog(this, "Piloto contratado exitosamente", "Exito", JOptionPane.INFORMATION_MESSAGE);

            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al contratar al profesor: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnContratarActionPerformed

